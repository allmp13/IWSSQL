SELECT
    F.L_FONCTION,
    U.C_SERVICE,
    U.N_UTIL,
    U.PRE_UTIL,
    S.L_FULLNAMESERVICE
FROM
    IWSPROD.suiteisilog.UTILISATEUR U INNER JOIN FONCTION F ON U.C_FONCTION=F.C_FONCTION INNER JOIN SERVICE S ON S.C_SERVICE=U.C_SERVICE
WHERE U.C_FONCTION IN ('50CHC','50CHS','40ACG') AND len(U.C_SERVICE)<>10

