select U.N_UTIL,* from UTILAFFECTHAB H, UTILISATEUR U
where I_HAB_RH <> 1 and H.C_UTIL=U.C_UTIL and H.T_UTILAFFECT <> 'D' 

select U.N_UTIL,U2.N_UTIL,* from UTILAFFECTHAB H, UTILISATEUR U, UTILISATEUR U2
where I_HAB_RH <> 1 and H.C_UTIL=U.C_UTIL and H.T_UTILAFFECT = 'D' and H.C_UTIL_DELEGANT=U2.C_UTIL 

select U.N_UTIL,* from UTILAFFECTHAB H, UTILISATEUR U
where I_HAB_RH <> 1 and H.C_UTIL=U.C_UTIL and H.T_UTILAFFECT = 'D' 

SELECT U.N_UTIL,count(H.C_PROFIL),H.C_FONCTION from UTILAFFECTHAB H,UTILISATEUR U WHERE T_UTILAFFECT = 'D' and H.C_PROFIL='CLIENT' and H.C_UTIL_DELEGANT=U.C_UTIL AND H.C_FONCTION is not null group by U.N_UTIL,H.C_FONCTION order by U.N_UTIL