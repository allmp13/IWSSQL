SELECT
    U.N_UTIL,
    H.C_FONCTION
FROM
    UTILAFFECTHAB H,
    UTILISATEUR U
WHERE H.T_UTILAFFECT = 'D'
    AND H.C_PROFIL = 'CLIENT' AND H.C_UTIL_DELEGANT=U.C_UTIL
ORDER BY U.N_UTIL,H.C_FONCTION

SELECT
    sum(Q_OBJET)
FROM
    suiteisilog.OBJET
WHERE Q_OBJET > 1
GROUP BY C_REF

SELECT
    O.C_REF,
    sum(O.Q_OBJET) AS QUANTITE
FROM
    suiteisilog.OBJET O
    INNER JOIN suiteisilog.SITE S ON S.C_SITE =O.C_SITE AND S.C_ROLESITE ='P'
WHERE Q_OBJET > 1
GROUP BY O.C_REF

