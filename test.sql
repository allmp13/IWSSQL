SELECT
    U.N_UTIL,
    U.I_UTI_MATRICULERH
FROM
    UTILISATEUR U
WHERE U.I_UTI_MATRICULERH IS  NULL

SELECT
    O.N_OBJET,
    O.I_OBJ_OCS_RESEAU_SDIS,
    O.I_OBJ_OCS_SITE_RESEAU,
    U.I_UTI_MATRICULERH,
    U.N_UTIL,
    PRE_UTIL
FROM
    (suiteisilog.OBJET O LEFT JOIN [suiteisilog].[CATALOGUE]  C ON C.C_REF=O.C_REF) INNER JOIN UTILISATEUR U ON U.C_UTIL=O.C_UTIL
WHERE O.C_NATURE='INF' AND C.C_FAMOBJ='IUNITE' AND O.C_STOBJ='EE'

SELECT
    C_SITE,
    C_SITE_PERE,
    C_SITEBARRE,
    N_SITE,
    T_LOVSITE
FROM
    SITE
WHERE C_ROLESITE='P'AND T_LOVSITE IS NOT NULL

SELECT
    C_SERVICE,
    C_SERVICE_PERE,
    N_SERVICE,
    C_SERVICEBARRE,
    *
FROM
    SERVICE
WHERE  (C_SERVICE_PERE <> 'ASTRE' OR C_SERVICE_PERE IS NULL) AND C_SERVICE <> 'ASTRE'


